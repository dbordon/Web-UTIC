<h2><span style="font-size: 10pt; font-family: arial, helvetica, sans-serif;"><strong><span style="color: #000000; background-color: #ffffff;">BREVE RESE&Ntilde;A HISTORICA</span></strong></span></h2>
<p style="margin-bottom: 0cm; font-variant: normal; letter-spacing: normal; font-style: normal; font-weight: normal; line-height: 150%; orphans: 2; widows: 2;"><span style="color: #000000; font-size: 10pt; font-family: arial, helvetica, sans-serif;">El Paraguay, desde la &eacute;poca colonial viene sosteniendo pesadas cargas socioculturales, a las que se fueron sumando ambiciones socioecon&oacute;micas e inestabilidad pol&iacute;tica tanto a nivel interno como externo.</span></p>
<p style="margin-bottom: 0cm; font-variant: normal; letter-spacing: normal; font-style: normal; font-weight: normal; line-height: 150%; orphans: 2; widows: 2;"><span style="color: #000000; font-size: 10pt; font-family: arial, helvetica, sans-serif;">A pesar de estas situaciones, logra crear a finales del siglo XIX, en el a&ntilde;o 1889 la primera instituci&oacute;n de Educaci&oacute;n Superior, la cual es la Universidad Nacional de Asunci&oacute;n. M&aacute;s tarde, ya en las d&eacute;cadas de los 60 del siglo XX, se funda la Universidad Cat&oacute;lica &ldquo;Nuestra Se&ntilde;ora de la de Asunci&oacute;n&rdquo;. Solo a partir de la promulgaci&oacute;n de la Constituci&oacute;n Nacional de 1992, en el Paraguay hubo apertura para la fundaci&oacute;n de nuevas universidades p&uacute;blicas y privadas, entre las cuales se halla la Universidad Tecnol&oacute;gica Intercontinental (UTIC) que fuera creada en 1996. De esta manera las universidades privadas pudieron abrir las ofertas educativas tambi&eacute;n a los ciudadanos del interior del pa&iacute;s, para responder a sus necesidades socioculturales, pol&iacute;ticas y econ&oacute;micas, muy poco atendidas anteriormente.</span></p>
<p style="margin-bottom: 0cm; font-variant: normal; letter-spacing: normal; font-style: normal; font-weight: normal; line-height: 150%; orphans: 2; widows: 2;"><span style="color: #000000; font-size: 10pt; font-family: arial, helvetica, sans-serif;">En respuesta de un sector profesional del pa&iacute;s, en el marco de la Ley N&ordm;822/96 de Universidades, el d&iacute;a viernes 12 de enero del a&ntilde;o 1996 es creada la Universidad Tecnol&oacute;gica Intercontinental (UTIC), bajo el patrocinio de la asociaci&oacute;n civil Escuela Tecnol&oacute;gica de Administraci&oacute;n (ETA). Esta asociaci&oacute;n ya ven&iacute;a trabajando desde el a&ntilde;o 1987, teniendo como primera sede el edificio ubicado sobre las calles Fulgencio R. Moreno esquina Yegros, donde tambi&eacute;n funcionaba el Rectorado y la Secretar&iacute;a General, habiendo iniciado con las carreras de Ciencias Empresariales, Licenciatura en An&aacute;lisis Inform&aacute;ticos y Derecho. Actualmente el edificio pertenece a la Sede de Derecho e Inform&aacute;tica de Asunci&oacute;n.</span></p>
<p style="margin-bottom: 0cm; font-variant: normal; letter-spacing: normal; font-style: normal; font-weight: normal; line-height: 150%; orphans: 2; widows: 2;"><span style="color: #000000; font-size: 10pt; font-family: arial, helvetica, sans-serif;">Con los retos de la globalizaci&oacute;n, e internacionalizaci&oacute;n, aparecen nuevas demandas para la formaci&oacute;n en la Educaci&oacute;n Superior paraguaya. En la actualidad se necesitan modelos pedag&oacute;gicos propios, acordes con los problemas contempor&aacute;neos, con las disciplinas que se ense&ntilde;an, con el manejo y las potencialidades de las nuevas tecnolog&iacute;as; en relaci&oacute;n con la cultura local y con las tendencias globales.</span></p>
<p style="margin-bottom: 0cm; font-variant: normal; letter-spacing: normal; font-style: normal; font-weight: normal; line-height: 150%; orphans: 2; widows: 2;"><span style="color: #000000; font-size: 10pt; font-family: arial, helvetica, sans-serif;">La universidad se ha comprometido con lo expresado en el documento sobre pol&iacute;ticas para el cambio y el desarrollo de la educaci&oacute;n superior de la UNESCO, publicado en el a&ntilde;o 1995, donde expresa en su art. 3, referido a la igualdad de acceso a la educaci&oacute;n, el documento emitido por la Conferencia Mundial sobre Educaci&oacute;n Superior de octubre del 98, en el que expresa de conformidad con el p&aacute;rrafo 1 del Art. 26&ordm; de la Declaraci&oacute;n Universal de los Derechos Humanos; &ldquo;el acceso a los estudios superiores deber&iacute;a estar basado en los m&eacute;ritos, la capacidad, los esfuerzos, la perseverancia y la determinaci&oacute;n de los aspirantes y en la perspectiva de la educaci&oacute;n a lo largo de la vida, podr&aacute; tener lugar a cualquier edad, tomando debidamente en cuenta las competencias adquiridas anteriormente, en consecuencia, en la educaci&oacute;n superior no se podr&aacute; admitir ninguna discriminaci&oacute;n fundada en la raza, el sexo, el idioma, la religi&oacute;n o consideraciones econ&oacute;micas, culturales o sociales, ni en capacidades f&iacute;sicas&rdquo;.</span></p>
<p style="margin-bottom: 0cm; font-variant: normal; letter-spacing: normal; font-style: normal; font-weight: normal; line-height: 150%; orphans: 2; widows: 2;"><span style="color: #000000; font-size: 10pt; font-family: arial, helvetica, sans-serif;">Respondiendo a este compromiso, se ha llevado adelante la creaci&oacute;n de sedes en el interior del pa&iacute;s, brindando la oportunidad de acceder a una formaci&oacute;n superior de calidad a la mayor cantidad posible de interesados.</span></p>
<p style="margin-bottom: 0cm; font-variant: normal; letter-spacing: normal; font-style: normal; font-weight: normal; line-height: 150%; orphans: 2; widows: 2;"><span style="color: #000000; font-size: 10pt; font-family: arial, helvetica, sans-serif;">En esta &uacute;ltima d&eacute;cada, se han realizado grandes inversiones en varias zonas del pa&iacute;s, adquiriendo y construyendo locales propios, habilitados hoy d&iacute;a en las ciudades de Asunci&oacute;n, Fernando de la Mora, San Lorenzo, Luque, Capiat&aacute;, Caacup&eacute;, Concepci&oacute;n, Horqueta, San Pedro del Ycuamandyy&uacute;, Santa Rosa del Aguaray, Encarnaci&oacute;n, Mar&iacute;a Auxiliadora, Coronel Oviedo, Caazap&aacute;, Caaguaz&uacute;, Ciudad del Este y Hernandarias. Estos locales cuentan con c&oacute;modas aulas y bibliotecas, as&iacute; como laboratorios equipados capaces de satisfacer las necesidades de los m&aacute;s exigentes programas de estudio.</span></p>
<p style="margin-bottom: 0cm; font-variant: normal; letter-spacing: normal; font-style: normal; font-weight: normal; line-height: 150%; orphans: 2; widows: 2;"><span style="color: #000000; font-size: 10pt; font-family: arial, helvetica, sans-serif;">Actualmente la UTIC tiene presencia en 19 ciudades, con 22 sedes y m&aacute;s de 11 mil estudiantes. Ha logrado hasta el momento 14 acreditaciones a nivel nacional, siendo la universidad privada con m&aacute;s certificaciones de calidad, siendo superado solamente por la Universidad Nacional de Asunci&oacute;n y la Universidad Cat&oacute;lica &ldquo;Nuestra Se&ntilde;ora de la Asunci&oacute;n&rdquo;.</span></p>
