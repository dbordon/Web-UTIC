            <p>La Sede Informática de Asunción fue la primera sede en crearse junto a la universidad, en fecha viernes 12 de enero de 1996, por resolución N° 6/96 del Consejo Superior Universitario. Además de contar con carreras de grado y programas de postgrado en el área informática, viene ofreciendo cursos cortos y económicos para los estudiantes interesados y para la comunidad, los cuales les ofrecen rápidas salidas laborales. </p>
 <p>La Sede de Derecho de Asunción fue fundada el día miércoles 12 de septiembre del año 2001, por resolución N° 69/2001 del Consejo Superior Universitario. Dispone de un Consultorio Jurídico Gratuito, el cual está llevando decenas de casos a personas de escasos recursos económicos. Además, cuenta con Maestrías en Derecho, que son programas creados a través de un acuerdo con el Consejo de la Magistratura.</p>
 <p>En el año 2018 las sedes de Informática y Derecho de Asunción se unieron en un local que corresponde al primer edificio donde funcionó la universidad, en donde cuenta actualmente con casi cuatrocientos estudiantes de ambas carreras.</p>

