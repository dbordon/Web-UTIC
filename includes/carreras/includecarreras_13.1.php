<h2><span style="color: #000000; background-color: #ffffff; font-size: 10pt;"><strong style="font-size: 13px;">PARA BACHILLERES</strong></span></h2>
<h2 style="text-align: left;"><span style="font-size: 12pt;"><strong><span style="font-size: 12pt; color: #000000; background-color: #ffffff; font-family: verdana,geneva;">PERFIL DE EGRESADO</span></strong></span></h2>
<h3 style="text-align: justify;"><span style="font-size: 12pt;"><span style="color: #000000; font-size: 12pt; font-family: verdana,geneva;"><span style="background-color: #ffffff;"><strong>El egresado tendr&aacute; la suficiente competencia de:</strong></span></span><span style="color: #000000; font-size: 12pt; font-family: verdana,geneva;"></span><span style="color: #000000; font-size: 12pt; font-family: verdana,geneva;"> </span></span></h3>
<ul style="text-align: center;">
<li style="text-align: justify;"><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Diagnosticar el hecho educativo en vista de mejorar la educaci&oacute;n y generar la transformaci&oacute;n social.</span></li>
<li style="text-align: justify;"><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Investigar y evaluar sobre la situaci&oacute;n socio-educativa en todos los niveles del sistema educativo vigente tanto en el &aacute;mbito nacional como internacional.</span></li>
<li style="text-align: justify;"><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Desarrollar y dirigir con eficiencia el proceso de ense&ntilde;anza &ndash; aprendizaje adapt&aacute;ndolo a las coyunturas educativas.</span></li>
<li style="text-align: justify;"><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Analizar, identificar y proponer soluciones a los problemas educativos en los diferentes niveles y modalidades del sistema educativo.</span></li>
<li style="text-align: justify;"><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Fundar y orientar la acci&oacute;n educativa mediante el uso racional de los medios modernos, cient&iacute;ficos y tecnol&oacute;gicos, los cuales estar&aacute;n encuadrados dentro de los procesos did&aacute;cticos-pedag&oacute;gicos.</span></li>
<li style="text-align: justify;"><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Asesorar a las instituciones educativas tanto en lo referente a la metodolog&iacute;a, a las estrategias pedag&oacute;gicas dentro del sistema educativo: formal y no formal.</span></li>
<li style="text-align: justify;"><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Promover el trabajo inter-disciplinario con el fin de mejorar la calidad, la equidad y la oportunidad educativa.</span></li>
<li style="text-align: justify;"><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Proyectar y gerenciar instituciones de ense&ntilde;anzas con un liderazgo transformador en vista de lograr la calidad en la educaci&oacute;n y, as&iacute;, beneficiar a toda la comunidad educativa.</span></li>
<li style="text-align: justify;"><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Mantener una actitud abierta y cr&iacute;tica entre la din&aacute;mica socio-pol&iacute;tico-cultural y las renovaciones educativas y pedag&oacute;gicas.</span></li>
<li style="text-align: justify;"><span style="color: #000000; font-family: verdana,geneva;"><span style="font-size: 10pt;">Preservar los valores &eacute;ticos, morales, democr&aacute;ticos y afianzar la identidad cultural: nacional y regional. <br /></span></span></li>
</ul>
<h2 style="font-size: 13px;"><span style="color: #000000; font-family: verdana,geneva; font-size: 12pt;">LICENCIATURA EN CIENCIAS DE LA EDUCACI&Oacute;N</span></h2>
<h2 style="font-size: 13px;"><span style="color: #000000; background-color: #ffffff; font-family: verdana,geneva; font-size: 12pt;">PARA PROFESORES</span></h2>
<h2><span style="color: #000000; font-family: verdana,geneva; font-size: 12pt;"><span style="line-height: 16px;"><strong>Objetivo General:</strong></span></span></h2>
<div><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;"><span style="line-height: 1.6em;">Formar profesionales competentes de la educaci&oacute;n en su campo de acci&oacute;n, que respondan a los desaf&iacute;os pedag&oacute;gicos y tecnol&oacute;gicos actuales, resuelvan interdisciplinariamente problemas educativos, realicen investigaciones en el &aacute;mbito de la realidad nacional y regional.</span></span></div>
<h2><span style="color: #000000; font-family: verdana,geneva; font-size: 12pt;"><strong>Objetivos espec&iacute;ficos:&nbsp; </strong></span></h2>
<p><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Adquirir conocimientos pedag&oacute;gicos, cient&iacute;ficos, tecnol&oacute;gicos y administrativos s&oacute;lidos para abordar con solvencias distintas situaciones educativas, que surgieren.</span></p>
<ul>
<li><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Emplear con idoneidad las nuevas metodolog&iacute;as y los recursos did&aacute;cticos en todos los niveles educativos.</span></li>
<li><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Utilizar las nuevas Tecnolog&iacute;as de Informaci&oacute;n y Comunicaci&oacute;n con el fin de aprovecharlas en el proceso de ense&ntilde;anza-aprendizaje de una manera eficaz.</span></li>
<li><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Elaborar, ejecutar y evaluar planes, programas y proyectos de educaci&oacute;n formal en su diferentes &aacute;reas y niveles del sistema educativo.</span></li>
<li><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Realizar investigaciones educativas en los diferentes niveles y modalidades para instituciones p&uacute;blicas y privadas.</span></li>
<li><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Investigar y analizar la problem&aacute;tica educativa para intervenir de modo creativo y eficiente en favor de su soluci&oacute;n mediante el dominio de las pol&iacute;ticas pedag&oacute;gicas y del curriculum del sistema educativo nacional y regional</span></li>
</ul>
<p><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Promover actividades de Investigaci&oacute;n Cient&iacute;fica en las &aacute;reas: Profesional Docente, Gesti&oacute;n Educativa y Tecnolog&iacute;a en vista de aportar conocimientos y mejorar la sociedad.</span><br /><span style="font-size: 12pt; color: #000000; background-color: #0000ff; font-family: verdana,geneva;"> </span></p>
<h2><span style="color: #000000; font-family: verdana,geneva; font-size: 12pt;"><strong><span style="background-color: #ffffff;">PERFIL DE EGRESADO</span></strong>&nbsp; </span></h2>
<h2><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;"><strong>El estudiante egresado de la carrera de Licenciatura en Ciencias de la Educaci&oacute;n de la Universidad Tecnol&oacute;gica Intercontinental es capaz de:</strong></span></h2>
<ul>
<li><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Participar e intervenir en la definici&oacute;n de pol&iacute;ticas educativas de distintos niveles de la Educaci&oacute;n paraguaya.</span></li>
<li><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Planificar, gestionar y evaluar proyectos educativos y actividades institucionales implementando.</span></li>
<li><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Realizar e implementar dise&ntilde;os de investigaci&oacute;n educativa, social, tecnol&oacute;gica y cultural en distintas situaciones de la comunidad.</span></li>
<li><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Impulsar la investigaci&oacute;n socio-educativa, la innovaci&oacute;n y la emprendibilidad conforme a los paradigmas pedag&oacute;gicos contempor&aacute;neos en vista de garantizar el aprendizaje significativo.</span></li>
<li><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Estructurar, propiciar, dirigir y evaluar de modo interdisciplinario proceso de ense&ntilde;anza-aprendizaje a la luz de las Nuevas Tecnolog&iacute;as de Informaci&oacute;n y Comunicaci&oacute;n.</span></li>
<li><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Proporcionar asesor&iacute;a pedag&oacute;gica a instituciones educativas y comunitarias.</span></li>
<li><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Emplear las herramientas tecnol&oacute;gicas en vista de crear nuevas estrategias did&aacute;cticas para lograr un aprendizaje significativo e integral.</span><br /><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;"> &nbsp;</span></li>
</ul>
<p><strong><span style="font-size: 10pt; color: #000000; background-color: #ffffff; font-family: verdana,geneva;">Requisitos para el ingreso a la Carrera Licenciatura en Ciencias de la Educaci&oacute;n son:</span></strong></p>
<ul>
<li><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Completar y firmar el Formulario de Solicitud de ingreso/ Ficha de Inscripci&oacute;n.</span></li>
<li><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Una copia autenticada ante Escriban&iacute;a del: T&iacute;tulo de Profesorado de los Niveles de: Educaci&oacute;n Escolar B&aacute;sica 1&ordm;, 2&ordm; y 3&ordm; ciclo o Educaci&oacute;n Media.</span></li>
<li><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Certificado de Estudio Original de Profesor/a de Educaci&oacute;n Escolar B&aacute;sica o Educaci&oacute;n Media.</span></li>
<li><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Dos copias simples de la C&eacute;dula de Identidad</span></li>
<li><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Un certificado de Nacimiento Original.</span></li>
<li><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Certificado de Estudios de Bachiller, visado y legalizado por el Ministerio de Educaci&oacute;n y Cultura del Paraguay.</span></li>
<li><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Una copia autenticada ante Escriban&iacute;a del T&iacute;tulo de Bachiller.</span></li>
<li><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Dos fotos tipo carnet.</span></li>
</ul>
<p><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Duraci&oacute;n de la carrera 30 meses con sustentaci&oacute;n de tesis.</span></p>

