<h3>Ciencias de la Comunicación</h3>
<p><strong>OBJETIVOS DE LA CARRERA</strong></p>

    <p>- Contribuir conjuntamente con otros actores sociales y espacios académicos a la democratización del campo socio-político de la comunicación, los medios y los procesos educativos específicos así como a su articulación con necesidades y problemáticas culturales plurales en el marco de la transformación del orden social dominante.</p>
    <p>- Desarrollar la construcción y reconstrucción de conocimientos socialmente relevantes en los procesos de formación de comunicadores y educadores para la comunicación, que profundicen las posibilidades de comprensión de las coordenadas socio-históricas actuales desde las ciencias humanas y los estudios de comunicación.</p>
	<p>- Promover a la superación de una perspectiva académica que constituye a la comunicación como objeto de estudio para pensarla como campo estratégico en la constitución de las dimensiones sociales, económicas, educativas, políticas y culturales, lo que demanda la definición de nuevas prácticas y saberes para accionar en ellas.</p>
	<p>- Generar un espacio educativo centrado en el protagonismo y la participación conjunta de docentes y alumnos en los procesos de enseñar y de aprender en tanto sujetos sociales desafiados por la comprensión y transformación crítica de su historia.</p>
	<p>- Articular creativa y críticamente los campos académico y profesional trascendiendo la perspectiva adaptativa y funcional, definiendo competencias profesionales relevantes desde un análisis complejo de las demandas sociales al campo comunicacional.</p>
	<p><strong>Perfil profesional</strong></p>
	<p>Conocer, planificar, articular, investigar y producir procesos y productos comunicacionales;</p>

	<p><strong>El egresado estará capacitado para:</strong></p>

<p>-Conocer, planificar, articular, investigar y producir procesos y productos comunicacionales;</p>
<p>-Comprender y analizar las dinámicas, las problemáticas y los procesos de comunicación;</p>
<p>-Elaborar, planificar y desarrollar prácticas comunicacionales en diferentes dimensiones y a través de distintos espacios sociales, culturales, políticos, institucionales, tecnológicos e interdisciplinarios;</p>
<p>-Relacionar en sus conocimientos y en su acción, las construcciones teóricas con las problemáticas de la práctica;</p>
<p>-Investigar, analizar, producir y crear críticamente, conocimiento para desenvolverse frente a los problemas de comunicación existentes;</p>
<p>-Intervenir en la integralidad social, desde la comprensión de las realidades locales en continuo diálogo con lo transnacional y lo global, poniendo en juego los conocimientos metodológicos, técnicos e instrumentales.</p>
<p>-Contribuir, desde su especificidad, en el campo de las comunicaciones aportando a los procesos de transformación social, diseñando y elaborando críticamente productos e instrumentos comunicacionales en diferentes lenguajes, formatos y soportes, por los diversos canales y medios de comunicación;</p>
<p>-Analizar –desde una perspectiva comunicacional y con herramientas inter y transdisciplinarias- los procesos sociales, económicos, políticos, culturales y de comunicación vinculados con todos los sectores, tanto a nivel internacional como nacional, departamental y municipal.</p>
<p>-Diagnosticar, diseñar, formular, ejecutar y evaluar procesos, planes, programas y proyectos de comunicación social en los diferentes espacios y desarrollos sociales, económicos, políticos y culturales de la sociedad;</p>
<p>-Desarrollar nuevas tecnologías de comunicación e intervenir en los procesos de producciones sociales, económicas, políticas y culturales que dichos fenómenos generen.</p>

<p><strong>PERFIL OCUPACIONAL</strong></p>
<p>-El Licenciado en Comunicación Social resultará competente, en función del perfil del título y de los contenidos curriculares de la carrera para el desarrollo de las siguientes actividades: la investigación, el conocimiento, la planificación, la articulación y la producción de procesos y productos comunicacionales;</p>

<p>-El análisis, a través de la comprensión, de las dinámicas y las problemáticas de los procesos de comunicación;</p>
<p>-La elaboración, la planificación y el desarrollo de prácticas comunicacionales en diferentes dimensiones y a través de distintos espacios sociales, culturales, políticos, institucionales, tecnológicos, interdisciplinarios;</p>
<p>-La integración, en estos procesos de acción de sus conocimientos teóricos con las problemáticas de las prácticas;</p>
<p>-La producción y creación crítica de conocimiento para desenvolverse frente a los problemas de comunicación existentes a través de la investigación y el análisis;</p>
<p>-La intervención comunicacional, desde la comprensión de las realidades locales en continuo diálogo con lo trasnacional y lo global, poniendo en juego los conocimientos me-todológicos, técnicos e instrumentales;</p>
<p>-El desarrollo de contribuciones y aportes, desde la especificidad del campo de las comunicaciones, a los procesos de transformación social a partir del diseño y elaboración crítica de productos e instrumentos comunicacionales, en diferentes lenguajes, formatos y soportes, por los diversos canales y medios de comunicación;</p>
<p>-El diagnóstico, diseño, formulación, ejecución y evaluación de procesos, planes, programas y proyectos de comunicación social en los diferentes espacios y desarrollos sociales, económicos. Políticos y culturales que dichos fenómenos generan.</p>
<p>-Contribución al desarrollo de nuevas tecnologías de comunicación y la integración de los nuevos procesos de producción social, económica, política y cultural que dichos fenómenos generen.</p>


