<h2><span style="font-size: 10pt;"><strong style="line-height: 16px; color: #000000; font-family: Helvetica,Arial,sans-serif; text-align: justify;"><span style="color: #2a5089; background-color: #ffffff; font-size: 12pt;">OBJETIVO GENERAL:</span><br /></strong></span></h2>
<p><span style="font-size: 10pt;"><span style="line-height: 1.6em;">Formar profesionales competentes tanto para el ejercicio de la docencia dentro del &aacute;rea de la especialidad en los distintos niveles del sistema educativo como tambi&eacute;n su incursi&oacute;n en todo tipo de proyecto estad&iacute;stico-financiero e investigaci&oacute;n cient&iacute;fica.</span></span></p>
<h2><span style="font-size: 10pt;"><strong style="font-size: 13px; color: #000000; font-family: Helvetica,Arial,sans-serif; line-height: 16px; text-align: justify;"><span style="font-size: 12pt; color: #2a5089; background-color: #ffffff;">OBJETIVOS ESPEC&Iacute;FICOS:</span><br /></strong></span></h2>
<ul>
<li><span style="font-size: 10pt;">Adquirir conocimiento amplio, integrado y profundo de los fundamentos te&oacute;ricos y pr&aacute;cticos de la Matem&aacute;tica Pura y Estad&iacute;stica-Financiera.</span></li>
<li><span style="font-size: 10pt;">Emplear con idoneidad la metodolog&iacute;a y la did&aacute;ctica de la matem&aacute;tica en todos los niveles educativos.</span></li>
<li><span style="font-size: 10pt;">Desarrollar y ejecutar actividades de Investigaci&oacute;n Cient&iacute;fica en el campo de la Matem&aacute;tica.</span></li>
<li><span style="font-size: 10pt;">Integrar y dirigir trabajos interdisciplinarios en el &aacute;mbito de la Matem&aacute;tica en pro del sistema educativo.</span></li>
</ul>
<h2><span style="font-size: 10pt;"><span style="font-size: 12pt; color: #2a5089; background-color: #ffffff;"><strong>PERFIL DEL EGRESADO:</strong></span><br /> <strong style="line-height: 1.6em;">El estudiante egresado de la carrera de Licenciatura en Matem&aacute;tica de la Universidad Tecnol&oacute;gica Intercontinental adquiere la capacidad para:</strong></span></h2>
<ul>
<li><span style="font-size: 10pt;">Desempe&ntilde;ar con amplitud y profundidad la docencia en todos los niveles educativos en las &aacute;reas de la Matem&aacute;tica.</span></li>
<li><span style="font-size: 10pt;">Ser experto en la disciplina de Matem&aacute;tica Pura o en la Matem&aacute;tica Estad&iacute;stica-Financiera.</span></li>
<li><span style="font-size: 10pt;">Impulsar proyecto de Investigaci&oacute;n Cient&iacute;fica disciplinar e interdisciplinar en el &aacute;mbito de cualquier &aacute;rea de la Matem&aacute;tica.</span></li>
<li><span style="font-size: 10pt;">Dise&ntilde;ar, asesorar y gestionar proyectos educativos e innovaciones did&aacute;cticas en las &aacute;reas de la Matem&aacute;tica para responder m&aacute;s efectivamente a las necesidades socio-econ&oacute;micas de la Naci&oacute;n.</span></li>
</ul>
<p><span style="font-size: 10pt;">Aplicar con idoneidad y profesionalismo los conocimientos de la Matem&aacute;tica adquiridos en cualquier entidad educativa, comercial y financiera.</span></p>
<p><br /><span style="color: #2a5089; background-color: #ffffff; font-size: 12pt;"><strong>LICENCIATURA EN MATEM&Aacute;TICA CON &Eacute;NFASIS EN MATEM&Aacute;TICA PURA</strong></span></p>
<p><span style="font-size: 12pt; background-color: #ffffff; color: #ffffff;"><strong style="line-height: 16px; color: #000000; font-family: Helvetica,Arial,sans-serif; text-align: justify;">Objetivos Espec&iacute;ficos:</strong></span></p>
<ul>
<li><span style="font-size: 10pt;">Tener una visi&oacute;n amplia de las distintas &aacute;reas de las matem&aacute;ticas pura en vista de ejercer la docencia</span></li>
<li><span style="font-size: 10pt;">Ejercer la docencia en los diferentes niveles del sistema educativo nacional.</span></li>
<li><span style="font-size: 10pt;">Realizar Investigaci&oacute;n Cient&iacute;fica dentro del &aacute;rea de la Matem&aacute;tica.</span></li>
<li><span style="font-size: 10pt;">Trabajar de modo interdisciplinario para actualizar programa curricular de ense&ntilde;anza media y/o universitaria en el &aacute;rea de las Matem&aacute;ticas y adaptarlos a las necesidades actuales del pa&iacute;s.</span></li>
<li><span style="font-size: 10pt;">Potenciar la ense&ntilde;anza de la matem&aacute;tica mediante t&eacute;cnicas y estrategias did&aacute;cticas sencillas para aplicarla en el quehacer cotidiano de los educandos.</span></li>
</ul>
<p><span style="font-size: 12pt;"><strong><span style="color: #2a5089; background-color: #ffffff;">LICENCIATURA EN MATEM&Aacute;TICA CON &Eacute;NFASIS EN MATEM&Aacute;TICA ESTAD&Iacute;STICA-FINANCIERA</span></strong></span></p>
<p><span style="font-size: 12pt;"><strong style="font-size: medium; font-family: 'Times New Roman',serif; color: #008000; line-height: 150%;">​</strong><strong style="color: #000000; font-family: Helvetica,Arial,sans-serif; line-height: 16px; text-align: justify;">Objetivos Espec&iacute;ficos:</strong></span></p>
<ul>
<li><span style="font-size: 10pt;">Aplicar conocimientos matem&aacute;ticos en diversos &aacute;mbitos como la econom&iacute;a, la administraci&oacute;n, la salud, la biolog&iacute;a, la ingenier&iacute;a, etc.</span></li>
<li><span style="font-size: 10pt;">Emprender tareas de Investigaci&oacute;n Cient&iacute;fica en las &aacute;reas de la Matem&aacute;tica Aplicada.</span></li>
<li><span style="font-size: 10pt;">Aplicar la t&eacute;cnicas y m&eacute;todos estad&iacute;sticos en diferentes entidades del sector privado y p&uacute;blico: bancos, cooperativas, financieras, industrias etc.</span></li>
<li><span style="font-size: 10pt;">Proporcionar asesoramiento, planificaci&oacute;n y evaluaci&oacute;n de proyectos empresariales, industriales y de producci&oacute;n.</span></li>
</ul>
<p><span style="font-size: 12pt;"><strong><span style="color: #2a5098;">​​<span style="text-decoration: underline;"><span style="line-height: 1.6em;">Requisito para el ingreso a la carrera:</span></span></span></strong></span><br /><span style="font-size: 10pt;"> </span></p>
<ul>
<li><span style="font-size: 10pt;"><span style="line-height: 1.6em;">Requis</span><span style="line-height: 1.6em;">itos para el ingreso a la Carrera Licenciatura en Matem&aacute;tica son:</span></span></li>
<li><span style="font-size: 10pt;">Completar y firmar el Formulario de Solicitud de ingreso/ Ficha de Inscripci&oacute;n.</span></li>
<li><span style="font-size: 10pt;">Una copia autenticada ante Escriban&iacute;a del: T&iacute;tulo de Profesorado de los Niveles de: Educaci&oacute;n Escolar B&aacute;sica 1&ordm;, 2&ordm; y 3&ordm; ciclo o Educaci&oacute;n Media.</span></li>
<li><span style="font-size: 10pt;">Certificado de Estudio Original de Profesor/a de Educaci&oacute;n Escolar B&aacute;sica o Educaci&oacute;n Media.</span></li>
<li><span style="font-size: 10pt;">Dos copias simples de la C&eacute;dula de Identidad</span></li>
<li><span style="font-size: 10pt;">Un certificado de Nacimiento Original.</span></li>
<li><span style="font-size: 10pt;">Certificado de Estudios de Bachiller, visado y legalizado por el Ministerio de Educaci&oacute;n y Cultura del Paraguay.</span></li>
<li><span style="font-size: 10pt;">Una copia autenticada ante Escriban&iacute;a del T&iacute;tulo de Bachiller.</span></li>
<li><span style="font-size: 10pt;">Dos fotos tipo carnet.</span></li>
<li><span style="font-size: 10pt;">Duraci&oacute;n de la carrera 36 meses con sustentaci&oacute;n de tesina.</span></li>
</ul>
