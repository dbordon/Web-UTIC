<p><strong>PERFIL PROFESIONAL:</strong></p>
<p style="text-align: justify;"><span style="font-family: verdana,geneva;">La carrera Pretende lograr el desarrollo de capacidades y aptitudes que acumulativamente, respondan a las siguientes l&iacute;neas fundamentales:</span></p>
<ul>
<li class="_mce_tagged_br" style="text-align: justify;"><span style="font-family: verdana,geneva;">Conocimiento de proceso judicial, en todas sus disciplinas.</span></li>
<li class="_mce_tagged_br" style="text-align: justify;"><span style="font-family: verdana,geneva;">Aprendizaje profundo del Derecho y sus nuevas ramas.</span></li>
<li class="_mce_tagged_br" style="text-align: justify;"><span style="font-family: verdana,geneva;">Formaci&oacute;n profesional para poder desenvolverse en la carrera judicial.</span></li>
<li class="_mce_tagged_br" style="text-align: justify;"><span style="font-family: verdana,geneva;">Ense&ntilde;anza activa para resolver problemas concretos en los diversos frentes que presenta el proceso judicial y los sistemas alternativos para la gesti&oacute;n y resoluci&oacute;n de conflictos.</span></li>
<li class="_mce_tagged_br" style="text-align: justify;"><span style="font-family: verdana,geneva;">Disposici&oacute;n para motivar capacidades de negociaci&oacute;n.</span></li>
<li class="_mce_tagged_br" style="text-align: justify;"><span style="font-family: verdana,geneva;">Ense&ntilde;anza de la &eacute;tica y el sentido de responsabilidad profesional.</span></li>
<li class="_mce_tagged_br" style="text-align: justify;"><span style="font-family: verdana,geneva;">Incentivaci&oacute;n de la capacidad creada y operativa en la formaci&oacute;n de emprendedores para la direcci&oacute;n y estrategia de empresas.</span></li>
<li class="_mce_tagged_br" style="text-align: justify;"><span style="font-family: verdana,geneva;">Vinculaci&oacute;n del abogado con diversos sectores de la producci&oacute;n nacional, procurando interactuar en la tarea de prevenci&oacute;n de conflictos.</span></li>
<li class="_mce_tagged_br" style="text-align: justify;"><span style="font-family: verdana,geneva;">Adecuada preparaci&oacute;n para las disputas que inevitablemente surgen del proceso judicial.</span></li>
<li class="_mce_tagged_br" style="text-align: justify;"><span style="font-family: verdana,geneva;">Formaci&oacute;n general, integral, y selectiva en cada alumno.</span></li>
<li class="_mce_tagged_br" style="text-align: justify;"><span style="font-family: verdana,geneva;">Manejo de idiomas y herramientas tecnol&oacute;gicas imprescindibles para el desarrollo profesional.</span></li>
<li class="_mce_tagged_br" style="text-align: justify;"><span style="font-family: verdana,geneva;">Formaci&oacute;n de profesionales investigadores.</span></li>
<li style="text-align: justify;"><span style="font-family: verdana,geneva;">Aptitudes solidarias para fomentar un desarrollo social cooperativo apoyado en los conocimientos y en las capacidades adquiridas.</span></li>
</ul>
