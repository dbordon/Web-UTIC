<h2 style="text-align: left;"><span style="background-color: #ffffff; font-family: verdana,geneva; font-size: 12pt;"><strong> Perfil de ingreso y egreso de la Carrera de Derecho:</strong></span></h2>
<p style="text-align: left;"><span style="font-size: 10pt;"><strong>De Ingreso: </strong></span></p>
<p style="text-align: left;"><span style="font-size: 10pt;"><strong>Para&nbsp; el ingreso a la Carrera de Derecho, es deseable que el interesado cuente&nbsp; con:</strong></span></p>
<h3 style="text-align: left;"><span style="font-size: 10pt; background-color: #ffffff;"><strong>CONOCIMIENTOS</strong></span></h3>
<p style="text-align: left;"><span style="font-family: verdana,geneva;">&nbsp;Dominio de los dos idiomas oficiales de nuestro pa&iacute;s en (escribir &ndash; hablar &ndash; leer y entender</span></p>
<ul style="text-align: left;">
<li><span style="font-family: verdana,geneva;">Conocimientos b&aacute;sicos de Historia y Geograf&iacute;a del Paraguay, Historia&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; de la Civilizaci&oacute;n,&nbsp;&nbsp; Filosof&iacute;a,</span></li>
<li><span style="font-family: verdana,geneva;">Formaci&oacute;n &Eacute;tica y Ciudadana, Sociolog&iacute;a, Psicolog&iacute;a, Econom&iacute;a y Antropolog&iacute;a Social que servir&aacute;n de</span></li>
<li><span style="font-family: verdana,geneva;">sustento te&oacute;rico a las asignaturas propias de&nbsp; la carrera.</span></li>
</ul>
<h3 style="text-align: left;">&nbsp;<span style="font-size: 10pt; background-color: #ffffff;"><strong>HABILIDADES</strong></span></h3>
<ul style="text-align: left;">
<li><span style="font-family: verdana,geneva;">El interesado&nbsp; evidenciar&aacute;&nbsp; el talento para trabajar en equipo.</span></li>
</ul>
<ul style="text-align: left;">
<li class="_mce_tagged_br"><span style="font-family: verdana,geneva;">Idoneidad para discutir, investigar y fundamentar&nbsp; los problemas sociales&nbsp; del contexto en que se desenvuelve.</span></li>
</ul>
<h3 style="text-align: left;"><span style="font-size: 10pt; background-color: #ffffff;"><strong>ACTITUDES</strong></span></h3>
<ul style="text-align: left;">
<li><span style="font-family: verdana,geneva;">Inter&eacute;s&nbsp; hacia la pr&aacute;ctica&nbsp; de la lectura e investigaci&oacute;n.</span></li>
</ul>
<ul style="text-align: left;">
<li class="_mce_tagged_br"><span style="font-family: verdana,geneva;">Disposici&oacute;n para el empleo del razonamiento cr&iacute;tico, l&oacute;gico,&nbsp; reflexivo y anal&iacute;tico de la problem&aacute;tica jur&iacute;dica en el an&aacute;lisis de casos.</span></li>
</ul>
<ul style="text-align: left;">
<li class="_mce_tagged_br"><span style="font-family: verdana,geneva;">Creatividad e iniciativa para integrase a equipos de trabajo.</span></li>
</ul>
<ul style="text-align: left;">
<li class="_mce_tagged_br"><span style="font-family: verdana,geneva;">Conducta sustentada en principios &eacute;ticos, morales, filos&oacute;ficos y democr&aacute;ticos.</span></li>
</ul>
<p style="text-align: left;"><span style="font-family: verdana,geneva;"><strong>De Egreso:</strong></span></p>
<p style="text-align: left;"><span style="font-family: verdana,geneva;">Como un profundo conocedor&nbsp; de las Ciencias Jur&iacute;dicas, que&nbsp;&nbsp; analiza, reflexiona y&nbsp; estudia&nbsp; la naturaleza, las funciones y los fundamentos propios de las normativas legales de nuestro pa&iacute;s y de la legislaci&oacute;n comparada, para su correcta y acertada&nbsp; aplicaci&oacute;n en su caso, el egresado de la Facultad de Derecho y Ciencias Sociales de la <strong>UNIVERSIDAD TECNOL&Oacute;GICA INTERCONTINENTAL</strong>, debe demostrar las siguientes aptitudes:</span></p>
<ul style="text-align: left;">
<li><span style="font-family: verdana,geneva;">Emplea y aplica sus conocimientos jur&iacute;dicos&nbsp; en sus intervenciones interpretando adecuadamente las normas, leyes del pa&iacute;s y de la legislaci&oacute;n comparada.</span></li>
</ul>
<ul style="text-align: left;">
<li class="_mce_tagged_br"><span style="font-family: verdana,geneva;">Demuestra su cultural general y jur&iacute;dica conforme a las exigencias legales en el ejercicio de la profesi&oacute;n.</span></li>
</ul>
<ul style="text-align: left;">
<li class="_mce_tagged_br"><span style="font-family: verdana,geneva;">Practica los principios&nbsp; &eacute;ticos y morales en sus funciones como auxiliar de la justicia.</span></li>
</ul>
<ul style="text-align: left;">
<li class="_mce_tagged_br"><span style="font-family: verdana,geneva;">Ejercita la solidaridad humana con esp&iacute;ritu de servicio ante la problem&aacute;tica de los desprotegidos (ni&ntilde;os, ancianos, mujeres y personas de escasos recursos econ&oacute;micos).</span></li>
</ul>
<ul style="text-align: left;">
<li class="_mce_tagged_br"><span style="font-family: verdana,geneva;">Act&uacute;a dentro de proceso con buena fe,&nbsp;&nbsp; buscando&nbsp; el bien, la justicia y la equidad en la relaci&oacute;n jur&iacute;dico procesal.</span></li>
</ul>
<ul style="text-align: left;">
<li class="_mce_tagged_br"><span style="font-family: verdana,geneva;">Asume con absoluta responsabilidad su compromiso en la resoluci&oacute;n de problemas jur&iacute;dicos dentro del &aacute;mbito p&uacute;blico y privado.</span></li>
</ul>
<ul style="text-align: center;">
<li class="_mce_tagged_br" style="text-align: left;"><span style="font-family: verdana,geneva;">Act&uacute;a como mediador, negociador, conciliador y&nbsp; arbitra en conflictos legales.</span></li>
</ul>

