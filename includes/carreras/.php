<p style="font-variant: normal; letter-spacing: normal; font-style: normal; font-weight: normal; line-height: 150%; orphans: 2; widows: 2; text-align: justify;"><span style="color: #00000a; font-size: 10pt;"><span style="font-family: Verdana, sans-serif;"><span style="text-decoration: none;">Habilidades que le faciliten la comprensi&oacute;n de conceptos abstractos, la resoluci&oacute;n de problemas emergentes, la comprensi&oacute;n del entorno de usuarios/clientes, la aplicaci&oacute;n de hardware y/o software, la &oacute;ptima comunicaci&oacute;n escrita y verbal, el desenvolvimiento de su iniciativa personal, su juicio y su sentido cr&iacute;tico, y la distinci&oacute;n entre lo esencial y lo accesorio frente al planteo de los elementos que, luego, conformar&aacute;n el sistema.</span></span></span></p>
