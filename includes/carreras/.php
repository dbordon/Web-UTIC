<p style="font-variant: normal; letter-spacing: normal; font-style: normal; line-height: 150%; orphans: 2; widows: 2; text-align: justify;"><span style="color: #00000a; font-size: 10pt;"><span style="font-family: Verdana, sans-serif;"><b><span style="text-decoration: none;">Segundo a&ntilde;o</span></b></span></span></p>
<p style="font-variant: normal; letter-spacing: normal; font-style: normal; font-weight: normal; line-height: 150%; orphans: 2; widows: 2; text-align: justify;"><span style="color: #00000a; font-size: 10pt;"><span style="font-family: Verdana, sans-serif;"><span style="text-decoration: none;">Desarrollar en los estudiantes los conocimientos suficientes para su desenvolvimiento profesional como programador de computadoras con las herramientas de programaci&oacute;n actuales.</span></span></span></p>
<p style="font-variant: normal; letter-spacing: normal; font-style: normal; line-height: 150%; orphans: 2; widows: 2; text-align: justify;"><span style="color: #00000a; font-size: 10pt;"><span style="font-family: Verdana, sans-serif;"><b><span style="text-decoration: none;">Tercer a&ntilde;o</span></b></span></span></p>
