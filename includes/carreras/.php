<h2 class="western" style="font-variant: normal; letter-spacing: normal; font-style: normal; line-height: 150%; orphans: 2; widows: 2; text-align: justify;"><span style="color: #00000a; font-size: 10pt;"><span style="font-family: Verdana, sans-serif;"><b><span style="text-decoration: none;">Fundamentaci&oacute;n</span></b></span></span></h2>
<p style="font-variant: normal; letter-spacing: normal; font-style: normal; font-weight: normal; line-height: 150%; orphans: 2; widows: 2; text-align: justify;"><span style="color: #00000a; font-size: 10pt;"><span style="font-family: Verdana, sans-serif;"><span style="text-decoration: none;">La informaci&oacute;n ha adquirido mayor valor del que tradicionalmente se le atribu&iacute;a. Hoy no solo contribuye a mejorar los procedimientos de las organizaciones si no que tiene un gran valor estrat&eacute;gico como herramienta de competitividad, al permitir el liderazgo en la reducci&oacute;n de costos y tiempos, as&iacute; como la identificaci&oacute;n y diferenciaci&oacute;n de segmentos del mercado, como catalizador incrementa la innovaci&oacute;n, permitiendo el trabajo conjunto de grupos separados geogr&aacute;ficamente a efectos de fortalecer los procesos decisivos. El mundo actual exige una adecuaci&oacute;n permanente a la tecnolog&iacute;a para la obtenci&oacute;n inmediata de la informaci&oacute;n, una empresa o profesional que no se encuentra acorde a las necesidades del mercado no es competitiva.</span></span></span></p>
<p style="font-variant: normal; letter-spacing: normal; font-style: normal; font-weight: normal; line-height: 150%; orphans: 2; widows: 2; text-align: justify;"><span style="color: #00000a; font-size: 10pt;"><span style="font-family: Verdana, sans-serif;"><span style="text-decoration: none;">Al hablar de sistemas de informaciones y llegar a ser competitivo nos referimos a la disponibilidad de informaciones exactas a la persona precisa en el momento y lugar oportuno. Destacando que la informaci&oacute;n es el activo m&aacute;s valioso de una organizaci&oacute;n tenemos por lo tanto que todo uso de herramientas de informaci&oacute;n y comunicaciones se orienta principalmente a la:</span></span></span></p>
<ul style="text-align: justify;">
<li style="font-variant: normal; letter-spacing: normal; font-style: normal; font-weight: normal; line-height: 150%; orphans: 2; widows: 2;"><span style="font-size: 10pt;"><span style="color: #00000a;"><span style="font-family: Verdana, sans-serif;"><span style="text-decoration: none;"></span></span></span><span style="color: #00000a;"><span style="font-family: Verdana, sans-serif;">Disponibilidad de la informaci&oacute;n</span></span></span></li>
<li style="font-variant: normal; letter-spacing: normal; font-style: normal; font-weight: normal; line-height: 150%; orphans: 2; widows: 2;"><span style="font-size: 10pt;"><span style="color: #00000a;"><span style="font-family: Verdana, sans-serif;"></span></span><span style="color: #00000a;"><span style="font-family: Verdana, sans-serif;">Consistencia de la informaci&oacute;n</span></span></span></li>
<li style="font-variant: normal; letter-spacing: normal; font-style: normal; font-weight: normal; line-height: 150%; orphans: 2; widows: 2;"><span style="font-size: 10pt;"><span style="color: #00000a;"><span style="font-family: Verdana, sans-serif;"></span></span><span style="color: #00000a;"><span style="font-family: Verdana, sans-serif;">Destinatario de la informaci&oacute;n</span></span></span></li>
<li style="font-variant: normal; letter-spacing: normal; font-style: normal; font-weight: normal; line-height: 150%; orphans: 2; widows: 2;"><span style="font-size: 10pt;"><span style="color: #00000a;"><span style="font-family: Verdana, sans-serif;"></span></span><span style="color: #00000a;"><span style="font-family: Verdana, sans-serif;">Oportunidad y Uso</span></span></span></li>
</ul>
<p style="font-variant: normal; letter-spacing: normal; font-style: normal; font-weight: normal; line-height: 150%; orphans: 2; widows: 2; text-align: justify;"><span style="font-size: 10pt;"><span style="color: #00000a;"><span style="font-family: Verdana, sans-serif;"></span></span><span style="color: #00000a;"><span style="font-family: Verdana, sans-serif;">La Universidad Tecnol&oacute;gica Intercontinental consciente de la situaci&oacute;n actual de mercado y la globalizaci&oacute;n del mundo ha optado por la creaci&oacute;n de la carrera de Licenciatura en An&aacute;lisis de Sistemas Inform&aacute;ticos con &eacute;nfasis en los sistemas de informaciones computarizados, teniendo como objetivo principal disponer conocimientos con los &uacute;ltimos avances tecnol&oacute;gicos, para el beneficio del profesional que egrese de esta casa de estudios.</span></span></span></p>
<p style="font-variant: normal; letter-spacing: normal; font-style: normal; font-weight: normal; line-height: 150%; orphans: 2; widows: 2; text-align: justify;"><span style="color: #00000a; font-size: 10pt;"><span style="font-family: Verdana, sans-serif;"><span style="text-decoration: none;">Es por ello que a trav&eacute;s de pr&aacute;cticas simuladas en laboratorios y en pasant&iacute;as profesionales se lograr&aacute; la formaci&oacute;n de profesionales potenciales de alto nivel capaces de competir a nivel nacional e internacional.</span></span></span></p>
<p style="font-variant: normal; letter-spacing: normal; font-style: normal; line-height: 150%; orphans: 2; widows: 2; text-align: justify;"><span style="color: #00000a; font-size: 10pt;"><span style="font-family: Verdana, sans-serif;"><b><span style="text-decoration: none;">Objetivos Generales de la Carrera</span></b></span></span></p>
<p style="font-variant: normal; letter-spacing: normal; font-style: normal; font-weight: normal; line-height: 150%; orphans: 2; widows: 2; text-align: justify;"><span style="color: #00000a; font-size: 10pt;"><span style="font-family: Verdana, sans-serif;"><span style="text-decoration: none;">Formar profesionales con los conocimientos te&oacute;ricos y pr&aacute;cticos suficientes, para proponer, analizar, dise&ntilde;ar, desarrollar, controlar y supervisar sistemas inform&aacute;ticos adecuados a las caracter&iacute;sticas espec&iacute;ficas o necesidades de entidades industriales, comerciales, de servicios, as&iacute; como la aplicaci&oacute;n de las herramientas inform&aacute;ticas para las investigaciones cient&iacute;ficas, que den como resultado una invenci&oacute;n o innovaci&oacute;n contribuyendo con ello al fortalecimiento tecnol&oacute;gico del pa&iacute;s.</span></span></span></p>
