<p style="font-variant: normal; letter-spacing: normal; font-style: normal; line-height: 150%; orphans: 2; widows: 2; text-align: justify;"><span style="color: #00000a; font-size: 10pt;"><span style="font-family: Verdana, sans-serif;"><b><span style="text-decoration: none;">Perfil Profesional</span></b></span></span></p>
