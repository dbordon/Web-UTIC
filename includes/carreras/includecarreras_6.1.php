<h2 style="text-align: justify;"><span style="background-color: #ffffff; font-family: verdana,geneva; font-size: 12pt;"><strong>FUNDAMENTOS:</strong></span></h2>
<p style="text-align: justify;"><span style="font-family: verdana,geneva; font-size: 10pt;">El desarrollo econ&oacute;mico, productivo y social de un pa&iacute;s se basa en gran medida en&nbsp; el nivel que se encuentra su capacidad agropecuaria y su ecosistema, ya que como actividad primaria, permite dotar a otras actividades humanas de los insumos para realizar su funci&oacute;n.</span></p>
<p style="text-align: justify;"><span style="font-family: verdana,geneva; font-size: 10pt;">Es&nbsp; as&iacute;,&nbsp; que con el prop&oacute;sito de formar&nbsp; profesionales con capacidad de tener bajo su custodia el estudio y el desarrollo de sistemas y programas que puedan ser utilizados en los agro negocios y las agroindustrias, enfoc&aacute;ndose a la alta productividad, el equilibrio ecol&oacute;gico y la correcta administraci&oacute;n de los mismos, surge la propuesta de la carrera de <strong>LICENCIATURA EN ADMINISTRACION AGROECOLOGICA E INDUSTRIAL</strong>, como alternativa v&aacute;lida para afrontar los desaf&iacute;os y exigencias de los nuevos tiempos.</span></p>
<h2 style="text-align: justify;"><span style="background-color: #ffffff; font-family: verdana,geneva; font-size: 12pt;"><strong>OBJETIVOS:</strong></span></h2>
<p style="text-align: justify;"><span style="font-family: verdana,geneva;"><strong><em>Formar profesionales aptos para:</em></strong></span></p>
<ul style="text-align: justify;">
<li><span style="font-family: verdana,geneva;">Administrar eficientemente empresas agrarias, evaluando y resolviendo cualquier problem&aacute;tica en el &aacute;mbito de la Administraci&oacute;n Agro ecol&oacute;gica e Industrial.</span></li>
<li><span style="font-family: verdana,geneva;">Orientar t&eacute;cnica y cient&iacute;ficamente el manejo de los recursos bi&oacute;ticos, para promover acciones de conservaci&oacute;n y auto-sost&eacute;n del medio ecol&oacute;gico.</span></li>
<li><span style="font-family: verdana,geneva;">Evaluar los niveles de impacto sobre los diferentes medios, generado por actividades agrarias, de urbanizaci&oacute;n y de explotaci&oacute;n.</span></li>
<li><span style="font-family: verdana,geneva;">Proponer y administrar programas de restauraci&oacute;n ecol&oacute;gica como alternativa a los problemas de degradaci&oacute;n ambiental, que se generan por actividades humanas.</span></li>
</ul>
<h2 style="text-align: justify;"><span style="background-color: #ffffff; font-family: verdana,geneva; font-size: 12pt;"><strong>PERFIL PROFESIONAL:</strong></span></h2>
<p style="text-align: justify;"><span style="font-family: verdana,geneva;">El<strong> LICENCIADO EN ADMINISTRACI&Oacute;N AGROECOLOGICA E INDUSTRIAL </strong>es creativo e innovador, consciente de la importancia de la ecolog&iacute;a, acorde a los nuevos desaf&iacute;os que depara el mercado nacional e internacional, habiendo adquirido conocimientos y desarrollado competencias para:</span></p>
<ul>
<li><span style="font-family: verdana,geneva;">Analizar, dise&ntilde;ar y desarrollar&nbsp; pol&iacute;ticas de producci&oacute;n y comercializaci&oacute;n agraria.</span></li>
<li><span style="font-family: verdana,geneva;">Ejecutar acciones para la conservaci&oacute;n del ecosistema, basando en el nuevo contexto legal para la protecci&oacute;n del ambiente dentro del pa&iacute;s.</span></li>
<li><span style="font-family: verdana,geneva;">Asesorar a productores o grupos&nbsp; de productores, cooperativas u otras asociaciones agr&iacute;colas.</span></li>
<li><span style="font-family: verdana,geneva;">Realizar investigaci&oacute;n de mercados, marketing estrat&eacute;gico y decisiones de precios.</span></li>
</ul>
<h2 style="text-align: justify;"><span style="background-color: #ffffff; font-family: verdana,geneva; font-size: 12pt;"><strong>CAMPO OCUPACIONAL</strong></span></h2>
<p style="text-align: justify;"><span style="font-family: verdana,geneva;">Los cargos ocupacionales que, por su preparaci&oacute;n, puede desempe&ntilde;ar el <strong>LICENCIADO EN</strong> <strong>ADMINISTRA</strong><strong>CI&Oacute;N</strong><strong> AGROECOLOGIC</strong><strong>A</strong><strong> E INDUSTRIAL, </strong>consiste en:</span></p>
<ul>
<li style="text-align: justify;"><span style="font-family: verdana,geneva;">Administraciones de&nbsp; campos</span></li>
<li style="text-align: justify;"><span style="font-family: verdana,geneva;">Asesor&iacute;as a empresas&nbsp; de control ecol&oacute;gico</span></li>
<li style="text-align: justify;"><span style="font-family: verdana,geneva;">Asesor&iacute;as en sistemas de comercializaci&oacute;n, relacionando al productor con los&nbsp; sistemas productivos, ecol&oacute;gicos e industriales.</span></li>
<li style="text-align: justify;"><span style="font-family: verdana,geneva;">Planeaci&oacute;n estrat&eacute;gica, para el relacionamiento de insumos y productos, con los t&eacute;cnicos espec&iacute;ficos, como el Agr&oacute;nomo o el Veterinario y con el productor agropecuario para lograr una combinaci&oacute;n arm&oacute;nica econ&oacute;micamente rentable y ecol&oacute;gicamente sustentable.</span></li>
