<p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;"><b>OBJETIVO GENERAL DE LA CARRERA</b></span></p>
<p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Formar profesionales universitarios, que posean los conocimientos, las capacidades y las habilidades requeridas para desempe&ntilde;arse en la actualidad de la actividad hotelera y tur&iacute;stica, para adecuarse a los cambios constantes de la globalizaci&oacute;n. Apto para actuar en mercados altamente competitivos, y que a la vez facilite la inserci&oacute;n del egresado en otros campos laborales afines.</span></p>
<p style="margin-bottom: 0cm; line-height: 150%;"><span style="font-family: arial, helvetica, sans-serif;"><b>OBJETIVOS ESPEC&Iacute;FICOS</b></span></p>
<ul>
<li style="margin-bottom: 0cm; line-height: 150%;"><span style="font-family: arial, helvetica, sans-serif;">Reconocer la importancia de la Industria tur&iacute;stica para el pa&iacute;s.</span></li>
<li style="margin-bottom: 0cm; line-height: 150%;"><span style="font-family: arial, helvetica, sans-serif;">Poseer conocimiento te&oacute;ricos pr&aacute;cticos de la actividad tur&iacute;stica y hotelera.</span></li>
<li style="margin-bottom: 0cm; line-height: 150%;"><span style="font-family: arial, helvetica, sans-serif;">Potenciar el turismo en sus diversas modalidades y adecuarse a los cambios constantes.</span></li>
<li style="margin-bottom: 0cm; line-height: 150%;"><span style="font-family: arial, helvetica, sans-serif;">Aprovechar racionalmente el desarrollo del turismo con enfoque integral, que permita la inserci&oacute;n en el campo laboral.</span></li>
<li style="margin-bottom: 0cm; line-height: 150%;"><span style="font-family: arial, helvetica, sans-serif;">Viabilizar canales de comercializaci&oacute;n.</span></li>
<li style="margin-bottom: 0cm; line-height: 150%;"><span style="font-family: arial, helvetica, sans-serif;">Asumir estrategias de emprendibilidad tendientes al desarrollo sustentable del turismo y a la preservaci&oacute;n del medio ambiente.</span></li>
<li style="margin-bottom: 0cm; line-height: 150%;"><span style="font-family: arial, helvetica, sans-serif;">Considerar los valores culturales de los recursos naturales.</span></li>
</ul>
<p style="margin-bottom: 0cm; line-height: 150%;"><span style="font-family: arial, helvetica, sans-serif;"><b>PERFIL DE EGRESO:</b></span></p>
<p style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">El <b>Licenciado/a en Hoteler&iacute;a y Turismo </b>se desenvolver&aacute; profesionalmente en la organizaci&oacute;n y administraci&oacute;n de empresas tur&iacute;sticas sustentables, y en la planificaci&oacute;n tur&iacute;stica, elaborando planes de manejo, dise&ntilde;ando proyectos tur&iacute;sticos, diagn&oacute;sticos tur&iacute;sticos, planes de mercadeo, paquetes, tours. Estas propuestas parten de una evaluaci&oacute;n de potencial y estudio de mercado. En los proyectos que proponga el licenciado/a, estar&aacute; capacitado para analizar la factibilidad, dise&ntilde;ar planes y estrategias de mercadeo e integrarlos a la comunidad.</span></p>
<p style="margin-bottom: 0cm; line-height: 150%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;"> La propuesta es formar profesionales pro activos y flexibles con una alta capacidad de adaptarse a los constantes cambios que marcan las megas tendencias de la industria del turismo y la hoteler&iacute;a que impulse el desarrollo de la actividad en relaci&oacute;n con el entorno relacionado con un turismo ambientalmente sustentable, que tiende a proteger y mejorar los recursos, para el desarrollo de nuevos productos tur&iacute;sticos tem&aacute;ticos e innovadores, y la implementaci&oacute;n de programas de calidad en empresas y destinos tur&iacute;sticos.<br /><br /></span></p>
<p style="margin-bottom: 0cm; line-height: 150%;"><span style="font-family: arial, helvetica, sans-serif;"><b>El Licenciado en Hoteler&iacute;a y Turismo tendr&aacute; capacidad para:</b></span></p>
<ul>
<li style="margin-bottom: 0cm; line-height: 150%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Comprender los principios del turismo: sus dimensiones espaciales, sociales, culturales, pol&iacute;ticas, laborales y econ&oacute;micas.</span></li>
<li style="margin-top: 0.49cm; margin-bottom: 0.49cm; line-height: 100%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Comprender el car&aacute;cter din&aacute;mico y evolutivo del turismo y de la nueva sociedad del ocio.</span></li>
<li style="margin-top: 0.49cm; margin-bottom: 0.49cm; line-height: 100%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Saber aplicar un enfoque de cambio e innovaci&oacute;n, para administrar las diferentes &aacute;reas de una empresa tur&iacute;stica.</span></li>
<li style="margin-top: 0.49cm; margin-bottom: 0.49cm; line-height: 100%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Comunicarse de forma oral y escrita en otras lenguas.</span></li>
<li style="margin-top: 0.49cm; margin-bottom: 0.49cm; line-height: 100%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Analizar, sintetizar y resumir cr&iacute;ticamente la informaci&oacute;n econ&oacute;mica- patrimonial de las empresas tur&iacute;sticas.</span></li>
<li style="margin-top: 0.49cm; margin-bottom: 0.49cm; line-height: 100%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Realizar investigaci&oacute;n de mercado para la promoci&oacute;n de productos tur&iacute;sticos y negocios tur&iacute;sticos en expansi&oacute;n.</span></li>
<li style="margin-top: 0.49cm; margin-bottom: 0.49cm; line-height: 100%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Planificar actividades de conservaci&oacute;n del medio ambiente y del patrimonio cultural.</span></li>
<li style="margin-top: 0.49cm; margin-bottom: 0.49cm; line-height: 100%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Comprender el funcionamiento de los destinos, estructuras tur&iacute;sticas y sus sectores empresariales en el &aacute;mbito mundial.</span></li>
<li style="margin-top: 0.49cm; margin-bottom: 0.49cm; line-height: 100%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Tener una marcada orientaci&oacute;n de servicio al cliente</span></li>
<li style="margin-top: 0.49cm; margin-bottom: 0.49cm; line-height: 100%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Desarrollar estudios de factibilidad para la creaci&oacute;n de nuevos proyectos tur&iacute;sticos y para la investigaci&oacute;n.</span></li>
<li style="margin-top: 0.49cm; margin-bottom: 0.49cm; line-height: 100%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Comprender el marco legal nacional e internacional, que regula las actividades tur&iacute;sticas.</span></li>
<li style="margin-top: 0.49cm; margin-bottom: 0.49cm; line-height: 100%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Adquirir habilidades comunicativas y aplicarlas en el &aacute;mbito interno y externo de las organizaciones tur&iacute;sticas.</span></li>
<li style="margin-top: 0.49cm; margin-bottom: 0.49cm; line-height: 100%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Saber gerenciar empresas prestadoras de servicios tur&iacute;sticos y hoteleros.</span></li>
<li style="margin-top: 0.49cm; margin-bottom: 0.49cm; line-height: 100%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Tener amplios conocimientos de las diversas expresiones culturales.</span></li>
<li style="margin-top: 0.49cm; margin-bottom: 0.49cm; line-height: 100%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Utilizar y analizar las tecnolog&iacute;as de la informaci&oacute;n y las comunicaciones (TIC) en los distintos &aacute;mbitos del sector tur&iacute;sticos.</span></li>
<li style="margin-top: 0.49cm; margin-bottom: 0.49cm; line-height: 100%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Usar eficientemente los recursos para permitir un aumento en la competitividad tur&iacute;stica.</span></li>
<li style="margin-top: 0.49cm; margin-bottom: 0.49cm; line-height: 100%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Promocionar y desarrollar destinos tur&iacute;sticos de acuerdo a las necesidades de este sector.</span></li>
<li style="margin-top: 0.49cm; margin-bottom: 0.49cm; line-height: 100%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Identificar y valorar la dimensi&oacute;n macroecon&oacute;mica y microecon&oacute;mica del turismo y los agentes econ&oacute;micos.</span></li>
<li style="margin-top: 0.49cm; margin-bottom: 0.49cm; line-height: 100%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Identificar y gestionar espacios y destinos tur&iacute;sticos.</span></li>
<li style="margin-top: 0.49cm; margin-bottom: 0.49cm; line-height: 100%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Dise&ntilde;ar gu&iacute;as, rutas, circuitos y paquetes tur&iacute;sticos difundiendo el potencial de los recursos naturales y culturales del Paraguay.</span></li>
<li style="text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Capacitador, consultor o asesor en turismo y empresas de hoteler&iacute;as.<br /><br /></span></li>
</ul>
<p><span style="font-family: arial, helvetica, sans-serif;"><b>CAMPO LABORAL </b>&nbsp;</span></p>
<p style="margin-bottom: 0cm; line-height: 150%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">El &aacute;mbito laboral de los graduados Licenciados en Hoteler&iacute;a y Turismo es muy amplio y variado. Podr&aacute; trabajar en:</span></p>
<ul>
<li style="margin-bottom: 0cm; line-height: 150%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Empresas del sector de Hoteler&iacute;a y Turismo como: Cadenas de hoteles, agencias de viajes, l&iacute;neas a&eacute;reas, restaurantes, operadores, centros de diversi&oacute;n, resort, spa y otros.</span></li>
<li style="margin-bottom: 0cm; line-height: 150%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Hospitales, sanatorios, geri&aacute;tricos.</span></li>
<li style="margin-bottom: 0cm; line-height: 150%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Organizaci&oacute;n de eventos, congresos, y otras actividades relacionadas a la Hoteler&iacute;a y Turismo.</span></li>
<li style="margin-bottom: 0cm; line-height: 150%; text-align: justify;"><span style="font-family: arial, helvetica, sans-serif;">Ministerios, Gobernaciones, Municipalidades, Centros Culturales, Organizaciones internacionales.</span></li>
<li style="margin-bottom: 0cm; line-height: 150%; text-align: justify;"><span style="font-family: Arial, sans-serif;"><span style="font-family: verdana,geneva;"><span style="font-family: arial, helvetica, sans-serif;">ONG, Clubes sociales, hoteles rurales, estancias y granjas dedicadas al turismo.</span></span></span><span style="font-family: Arial, sans-serif;"><span style="font-family: verdana,geneva;"><br /></span></span><span style="font-family: Arial,sans-serif; font-size: 10pt;"><span style="font-family: verdana,geneva;"><br /></span></span></li>
</ul>

