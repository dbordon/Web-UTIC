<li><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Conocimientos s&oacute;lidos de las principales aproximaciones al estudio cient&iacute;fico del comportamiento humano, as&iacute; como de los &aacute;mbitos y las limitaciones de la aplicaci&oacute;n de su especialidad.</span></li>
<li><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Facilidad para comprender a las personas.</span></li>
