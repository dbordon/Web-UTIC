</ul>
<h2><span style="font-size: 12pt; background-color: #ffffff; font-family: verdana,geneva; color: #000000;"><strong>ACTIVIDAD PROFESIONAL</strong></span></h2>
<p style="text-align: justify;"><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">El Licenciado en Psicolog&iacute;a deber&aacute; detectar y diagnosticar procesos de comportamiento en individuos, grupos o comunidades, en &aacute;mbitos educativos o empresariales, donde dise&ntilde;ar&aacute;, aplicar&aacute; y evaluar&aacute; las estrategias e instrumentos de diagn&oacute;sticos e intervenci&oacute;n destinados a mejorar dichos procesos.</span></p>
<p style="text-align: justify;"><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">El egresado deber&aacute; identificar, analizar y evaluar las teor&iacute;as derivadas de las orientaciones psicol&oacute;gicas, buscando intervenir dentro de un contexto cultural, pol&iacute;tico y econ&oacute;mico, fundament&aacute;ndose en el marco de la investigaci&oacute;n cient&iacute;fica. A fin de reproducir y /o actualizar los conocimientos te&oacute;ricos, metodol&oacute;gicos y t&eacute;cnicos de la disciplina.</span></p>
<p style="text-align: justify;"><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">Cualquiera sea su especialidad, el egresado de la carrera de Licenciatura en Psicolog&iacute;a ha de colaborar en los procesos que involucren al medio f&iacute;sico y social del individuo, y la integraci&oacute;n de la conducta: contaminaci&oacute;n y equilibrio del medio ambiente, impacto de la derogaci&oacute;n ecol&oacute;gica en el individuo, propaganda y concienciaci&oacute;n de la sociedad, planeaci&oacute;n y fomento de programas de deterioro ambiental, estr&eacute;s, hacinamiento, vivienda, construcci&oacute;n y evaluaci&oacute;n conductual de situaciones de emergencia ambiental; en los procesos psicosociales de la comunicaci&oacute;n: medios de comunicaci&oacute;n y su repercusi&oacute;n en la conducta, programaci&oacute;n del tiempo libre, comunicaci&oacute;n Organizacional, comunicaci&oacute;n educativa, comunicaci&oacute;n intrafamiliar, publicidad y propaganda, opini&oacute;n, dise&ntilde;o y formaci&oacute;n de auditorio, etc.</span></p>
<p style="text-align: justify;"><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">El Licenciado en Psicolog&iacute;a Educacional participa o interviene en los procesos educativos desde el punto de vista psicol&oacute;gico tales como: problemas de ense&ntilde;anza-aprendizaje, m&eacute;todos did&aacute;cticos, orientaci&oacute;n escolar-vocacional-profesional, educaci&oacute;n especial, evaluaci&oacute;n educativa y planeaci&oacute;n educativa.</span></p>
<p style="text-align: justify;"><span style="font-size: 10pt; color: #000000; font-family: verdana,geneva;">El Licenciado en Psicolog&iacute;a Empresarial participa e interviene en los procesos laborales y de consumo, tales como: procesos de reclutamiento y selecci&oacute;n de personal; elaboraci&oacute;n e implementaci&oacute;n de manuales de organizaci&oacute;n, capacitaci&oacute;n y adiestramiento, consultor&iacute;a e intervenci&oacute;n institucional, higiene y seguridad industrial, procesos administrativos, ergonom&iacute;a, concienciaci&oacute;n del consumidor y mercadotecnia.</span></p>


