<p>
La Consejería Psicológica es un proceso a través del cual el consejero ayuda a las personas que recurren a él a obtener una mayor comprensión o entendimiento de lo que le sucede, a conocerse un poco más e identificar los recursos con los que cuenta para ayudarse a sí mismo, está basado en técnicas de escucha, comunicación y habilidades interpersonales.      </font>

</p>
<p>
En algunos casos, cuando la Consejería Psicológica no baste para alcanzar los objetivos que la persona está buscando, o el psicólogo considere que se requiere otro tipo de abordaje para ayudarlo/la, este sugerirá que el mismo acuda a psicoterapia y/o atención psiquiátrica, contando con una red de contactos para derivarlos a profesionales de estas u otras disciplinas.
</p>
<p>
Con respecto al Programa de Actualización y Capacitación a la Comunidad Educativa, se elabora un calendario de manera a realizarlas a través de talleres, conferencias, cursos para la promoción y prevención de la Salud Mental con profesionales del área.
</p>
